for (int a = 0; a < aa; a++)             // Обьявляем цикл for отвечающий за строки (условие пока а меньше переменной аа, т.е. длинны массива)

{

    for (int b = 0; b < bb; b++)         //Обьявляем цикл for отвечающий за количество столбцов(Условие пока в меньше вв)
    {
        if (a == b)
        {                       //Обьявяем условие if если в массиве а=в, то значение будет задано 1 или(else) 0(по умолчанию int равен 0)
            array[a, b] = 1;               // именно так отображается символы по диагонали
            Console.Write($"{array[a, b]}" + "\t");
        }
        else
        {
            Console.Write($"{array[a, b]}" + "\t");
        }

    }

    Console.WriteLine();

}




