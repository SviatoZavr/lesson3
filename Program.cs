int bb = 10;          // Определим переменную и задаем количество столбцов
int[,] array = new int[aa, bb];        //  Задаем массив array и его разрядность

// Не получилось задать массив через int[,]array=[2,10]; При выполении отладке ругался что превышен ранг(out of range).
